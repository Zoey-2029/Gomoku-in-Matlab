time = 0;

timer_start_btn = uibutton(fig, 'push', ...
                   'Text', 'START', ...
                   'Position',[200, 365, 80, 20], ...
                   'ButtonPushedFcn', @(timer_start_btn,event) start_timer());
                
timer_reset_btn = uibutton(fig, 'push', ...
                   'Text', 'RESET', ...
                   'Position',[300, 365, 80, 20], ...
                   'ButtonPushedFcn', @(timer_reset_btn,event) reset_timer(input));

if (isscalar(input)) % check for the input
    time = input;
end

