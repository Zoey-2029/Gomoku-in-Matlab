                    'Text', time_str, ...
                    'Position', [120, 390, 330, 30], ...
                    'BackgroundColor', [0.9 0.9 0.9], ...
                    'FontSize', 20, ...
                    'FontColor', 'r', ...
                    'FontName', 'Menlo', ...
                    'FontWeight', 'bold', ...
                    'HorizontalAlignment', 'center');

